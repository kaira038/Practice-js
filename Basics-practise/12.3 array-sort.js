//SORT METHODS

// 1. Alpabetic Sort

//1.1 sort()
// sorts an array alphabetically
const fruits = ["Apple", "orange", "mango", "berries"];
let val1 = fruits.sort();

console.log(val1);  //------->[ "Apple", "berries", "mango", "orange" ]


//1.2 reverse()
// reverses the elements in an array
let val2 = fruits.reverse();

console.log(val2);  //------> [ "orange", "mango", "berries", "Apple" ]






