// JavaScript syntax is the set of rules

//create variable
let x;
var y;

//use variable
x = 3;
y = 6;
let z = x + y;


// 1. FIXED VALUES are called "Literals".
// most important syntax rules for fixed values are:
// 1.1. Numbers are written with or without decimals:
let num1 = 21;
let num2 = 21.56;

console.log(num1);
console.log(num2);
// 2.1. Strings are text, written within double or single quotes:
let string1 = "kavi";
let string2 = 'priya';

console.log(string1);
console.log(string2);


// 2. VARIABLES values are called "Variables".
// variables are used to store data values.
// JavaScript uses the keywords var, let and const to declare variables
// equal sign is used to assign values to variables.
let a;  //declare a variable
a = 3;   // assign a value to the declared variable

console.log(a);


// 3.JAVASCRIPT OPERATORS
//3.1  arithmetic operators ( + - * / ) to compute values:
let c = (5 + 6) * 10;

console.log(c); //110 


// 3.2 assignment operator ( = ) to assign values to variables:
let m, n;
m = 5;
n = 6;
val = n - m;

console.log(val);


// 4. JAVASCRIPT EXPREESSION
// An expression is a combination of values, variables, and operators, which computes to a value.
// computation is called an evaluation.
let p = 5 * 7;
let q = p - 2;
let r = "kavi" + " " + "priya" + "is" + " " + 21 + "/" + "age";

console.log(p);
console.log(q);
console.log(r);
// (or)
console.log(p, q, r);


